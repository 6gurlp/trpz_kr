package org.example.jenkinstool.pipeline.execution.definition;

public class StageDefinition {

    private String name;
    private String run;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRun() {
        return run;
    }

    public void setRun(String run) {
        this.run = run;
    }
}
